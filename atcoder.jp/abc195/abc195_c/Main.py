import math
n = int(input())
m = int(math.log10(n)//1)
cnt = 0

if 3 <= m and m < 6:
    cnt += n - (10**3 - 1)
if 6 <= m and m < 9:
    cnt += 2*(n-(10**6-1)) + 1*((10**6 - 1) - (10**3 - 1)) 
if 9 <= m and m < 12:
    cnt += 3*(n-(10**9-1)) + 2*((10**9 - 1) - (10**6 - 1))+ 1*((10**6 - 1) - (10**3 - 1))
if 12 <= m and m < 15:
    cnt += 4*(n-(10**12-1)) + 3*((10**12 - 1) - (10**9 - 1)) + 2*((10**9 - 1) - (10**6 - 1))+ 1*((10**6 - 1) - (10**3 - 1))
if 15 <= m:
    cnt += 5*(n-(10**15-1)) + 4*((10**15 - 1) - (10**12 - 1)) + 3*((10**12 - 1) - (10**9 - 1)) + 2*((10**9 - 1) - (10**6 - 1))+ 1*((10**6 - 1) - (10**3 - 1))   
print(cnt)    